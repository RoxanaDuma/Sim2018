package it.polito.tdp.flightdelays.model;

public class CoppiaAirports {

	private int airportId1;
	private int airportId2;
	
	public CoppiaAirports(int airportId1, int airportId2) {
		super();
		this.airportId1 = airportId1;
		this.airportId2 = airportId2;
	}

	public int getAirportId1() {
		return airportId1;
	}

	public void setAirportId1(int airportId1) {
		this.airportId1 = airportId1;
	}

	public int getAirportId2() {
		return airportId2;
	}

	public void setAirportId2(int airportId2) {
		this.airportId2 = airportId2;
	}
	
	
	
	
	
}
